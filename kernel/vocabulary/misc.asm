; ***************************************************************************************
; 							fill (byte address count - )
; ***************************************************************************************

; @forth.ix 	fill
		pop 	hl 								; DE = count, HL = address
		pop 	bc 								; C = byte	
		ld 		a,d
		or 		e
		jr 		z,__fillExit
__fillLoop:
		ld 		(hl),c
		inc 	hl
		dec 	de
		ld 		a,d
		or 		e
		jr 		nz,__fillLoop
__fillExit:
		pop 	de 								; restore TOS
; @end

